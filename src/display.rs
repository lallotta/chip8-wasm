pub struct Display {
    pub gfx: [u8; 2048],
}

pub const WIDTH: usize = 64;
pub const HEIGHT: usize = 32;

impl Display {
    pub fn new() -> Display {
        Display {
            gfx: [0; 2048]
        }
    }

    pub fn get_index(&self, x: usize, y: usize) -> usize {
        y * WIDTH + x
    }

    pub fn clear(&mut self) {
        for i in 0..self.gfx.len() {    
            self.gfx[i] = 0;
        }
    }
}

